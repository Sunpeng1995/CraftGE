#include "Mesh.h"

void Mesh::addTexture(Texture* tex) {
  mTextures.push_back(tex);
}

Texture* Mesh::getTexture(int idx) {
  if (idx < 0 || idx >= mTextures.size()) {
    return nullptr;
  }
  return mTextures[idx];
}

void Mesh::setScale(float scale) {
  mScale = scale;
}

void Mesh::setPos(glm::vec3 pos) {
  mPos = pos;
}

