void Cube::draw(Shader* shader) {
  glm::mat4 model;
  model = glm::translate(model, mPos);
  model = glm::rotate(model, (float)glfwGetTime() * glm::radians(mRotateAngle), mRotateAxis);
  shader->setMat4("model", model);

  Texture* tex1 = getTexture(0);
  Texture* tex2 = getTexture(1);

  if (tex1) {
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_2D, tex1->getTextureID());
    shader->setInt("texture0", 0);
  }
  if (tex2) {
    glActiveTexture(GL_TEXTURE1);
    glBindTexture(GL_TEXTURE_2D, tex2->getTextureID());
    shader->setInt("texture1", 1);
  }

  shader->setFloat("mixer", 0.2f);

  glBindVertexArray(mVAO);
  glDrawArrays(GL_TRIANGLES, 0, 36);
  glBindVertexArray(0);
}

void Cube::setRotate(float angle, glm::vec3 axis) {
  mRotateAngle = angle;
  mRotateAxis = axis;
}